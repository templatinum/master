    // generate_function_index
    //   method: creates an alphabetical index of all
    // functions.
    wiki_page generate_function_index(
        const std::vector<const code_symbol*>& _fns)
    {
        std::string content;

        content += wiki_markup::heading(
            "Function Index", 1);
        content += "\n";

        if (_fns.empty())
        {
            content += wiki_markup::italic(
                "No functions found.") + "\n";
        }
        else
        {
            content += wiki_markup::table_start();
            content += wiki_markup::table_header(
                {"Function", "Return Type",
                 "Description"});

            for (const auto* sym : _fns)
            {
                content += wiki_markup::table_row({
                    wiki_markup::code_inline(
                        sym->name),
                    wiki_markup::code_inline(
                        sym->return_type.empty()
                            ? "void"
                            : sym->return_type),
                    sym->comment.text
                });
            }

            content += wiki_markup::table_end();
        }

        content += "\n";

        if (!m_config.default_category.empty())
        {
            content += wiki_markup::category(
                m_config.default_category);
        }

        content +=
            wiki_markup::category("Functions");

        // bindings
        std::unordered_map<std::string, std::string>
            bindings;

        bindings["content"] = content;

        // assemble
        wiki_page page;

        page.title   = make_title("Function Index");
        page.kind    =
            wiki_page_kind::function_index;
        page.content = render_layout(
            D_WIKI_LAYOUT_FUNCTION_INDEX,
            bindings, content);

        page.categories.push_back("Functions");

        return page;
    };


    // =========================================================
    // MEMBER SECTIONS
    // =========================================================

    // generate_member_sections
    //   method: generates member tables grouped by access
    // specifier and symbol kind.
    std::string generate_member_sections(
        const std::vector<code_symbol>& _members)
    {
        std::string content;

        // group: types (typedefs, aliases, nested)
        std::string types = generate_member_table(
            _members, "Types",
            [](const code_symbol& _s)
            {
                return ( (_s.kind ==
                    code_symbol_kind::typedef_decl) ||
                    (_s.kind ==
                    code_symbol_kind::type_alias) ||
                    (_s.kind ==
                    code_symbol_kind::class_decl) ||
                    (_s.kind ==
                    code_symbol_kind::struct_decl) ||
                    (_s.kind ==
                    code_symbol_kind::enum_decl) );
            });

        // group: methods
        std::string methods = generate_member_table(
            _members, "Methods",
            [](const code_symbol& _s)
            {
                return ( (_s.kind ==
                    code_symbol_kind::method_decl) ||
                    (_s.kind ==
                    code_symbol_kind::constructor) ||
                    (_s.kind ==
                    code_symbol_kind::destructor) ||
                    (_s.kind ==
                    code_symbol_kind::operator_decl) );
            });

        // group: data members
        std::string fields = generate_member_table(
            _members, "Data Members",
            [](const code_symbol& _s)
            {
                return ( (_s.kind ==
                    code_symbol_kind::field_decl) ||
                    (_s.kind ==
                    code_symbol_kind::variable_decl) );
            });

        content += types;
        content += methods;
        content += fields;

        return content;
    };

    // generate_member_table
    //   method: generates a table of members matching a
    // filter predicate.
    std::string generate_member_table(
        const std::vector<code_symbol>& _members,
        const std::string& _heading,
        std::function<bool(const code_symbol&)> _filter)
    {
        // collect matching members
        std::vector<const code_symbol*> matched;

        for (const auto& member : _members)
        {
            if (_filter(member))
            {
                matched.push_back(&member);
            }
        }

        if (matched.empty())
        {
            return std::string();
        }

        std::string content;

        content += wiki_markup::heading(_heading, 3);

        content += wiki_markup::table_start();

        std::vector<std::string> headers =
            {"Name", "Signature", "Description"};

        if (m_config.show_access)
        {
            headers.insert(
                headers.begin(), "Access");
        }

        content += wiki_markup::table_header(headers);

        for (const auto* sym : matched)
        {
            std::vector<std::string> cells;

            if (m_config.show_access)
            {
                cells.push_back(
                    wiki_markup::access_string(
                        sym->access));
            }

            cells.push_back(
                wiki_markup::code_inline(sym->name));
            cells.push_back(
                wiki_markup::code_inline(
                    sym->signature));
            cells.push_back(sym->comment.text);

            content += wiki_markup::table_row(cells);
        }

        content += wiki_markup::table_end();
        content += "\n";

        return content;
    };


    // =========================================================
    // TITLE HELPERS
    // =========================================================

    // make_title
    //   method: constructs a wiki page title from a symbol
    // name, applying the configured prefix.
    std::string make_title(
        const std::string& _name) const
    {
        if (m_config.title_prefix.empty())
        {
            return _name;
        }

        return m_config.title_prefix + _name;
    };


