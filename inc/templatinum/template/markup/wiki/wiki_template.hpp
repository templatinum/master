/******************************************************************************
* templatinum [template]                                     wiki_template.hpp
*
* MediaWiki page generator from parsed code (C++).
*   Transforms a code_parse_result into a collection of MediaWiki-formatted
* pages organized as a full documentation hierarchy: module overview, class
* pages, namespace pages, enum pages, and a function index.
*
*   Default page layouts are built-in as MediaWiki markup strings rendered
* through tmpl_text_template. Each page kind has a named layout that can
* be overridden by the user at runtime, enabling full customization of the
* wiki output without modifying the template itself.
*
*   The output is a wiki_output containing a vector of named wiki_page
* values. Each page carries its title, content, kind, and categories.
* Downstream consumers (beams targeting a wiki API, file writers, etc.)
* can iterate the pages and route them independently.
*
* COMPONENTS:
*   - templatinum::wiki_page_kind - enum: module_overview, class_page, 
*       namespace_page, enum_page, function_index
*   - templatinum::wiki_page - a single generated wiki page
*   - templatinum::wiki_output - collection of wiki pages with lookup helpers
*   - templatinum::wiki_config - generation configuration
*   - templatinum::wiki_markup - static MediaWiki markup formatting helpers
*   - templatinum::tmpl_wiki_template - 
*       CRTP template: code_parse_result -> wiki_output
*
* FEATURE DEPENDENCIES:
*   D_ENV_CPP_FEATURE_LANG_RVALUE_REFERENCES   - move semantics
*   D_ENV_CPP_FEATURE_LANG_ALIAS_TEMPLATES     - using aliases
*   D_ENV_CPP_FEATURE_LANG_LAMBDAS             - lambda closures
*
* PORTABLE ACROSS:
*   C++11, C++14, C++17, C++20, C++23, C++26
*
* 
* path:      /inc/templatinum/template/markup/wiki/wiki_template.hpp
* author(s): Samuel 'teer' Neal-Blim                          date: 2026.03.26
******************************************************************************/

