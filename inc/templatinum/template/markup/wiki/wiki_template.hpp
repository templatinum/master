///////////////////////////////////////////////////////////////////////////////
///             I.    PAGE CLASSIFICATION                                   ///
///////////////////////////////////////////////////////////////////////////////

// wiki_page_kind
//   enum: the type of wiki page generated.
enum class wiki_page_kind
{
    // top-level overview page listing all modules,
    // classes, and namespaces in the parsed source
    module_overview,

    // dedicated page for a class or struct
    class_page,

    // dedicated page for a namespace
    namespace_page,

    // dedicated page for an enum
    enum_page,

    // alphabetical index of all functions/methods
    function_index,

    // custom page generated by user-defined logic
    custom
};


///////////////////////////////////////////////////////////////////////////////
///             II.   WIKI PAGE                                             ///
///////////////////////////////////////////////////////////////////////////////

// wiki_page
//   struct: a single generated wiki page carrying its title,
// MediaWiki markup content, kind, and category tags.
struct wiki_page
{
    // page title (used as the wiki page name)
    std::string title;

    // full MediaWiki markup content
    std::string content;

    // the kind of page this is
    wiki_page_kind kind;

    // MediaWiki categories (e.g., "Classes", "API")
    std::vector<std::string> categories;

    // the source symbol's qualified name (empty for
    // overview/index pages)
    std::string source_symbol;

    wiki_page()
        : title()
        , content()
        , kind(wiki_page_kind::custom)
        , categories()
        , source_symbol()
    {
    };
};


///////////////////////////////////////////////////////////////////////////////
///             III.  WIKI OUTPUT                                           ///
///////////////////////////////////////////////////////////////////////////////

// wiki_output
//   struct: the aggregate output of a wiki generation pass.
// Contains all generated pages plus metadata.
struct wiki_output
{
    // all generated pages
    std::vector<wiki_page> pages;

    // source file that was parsed (for provenance)
    std::string source_file;

    // true if generation completed without errors
    bool success;

    wiki_output()
        : pages()
        , source_file()
        , success(false)
    {
    };

    // page_count
    //   method: returns the total number of generated pages.
    std::size_t page_count() const
    {
        return pages.size();
    };

    // find
    //   method: finds a page by title. Returns nullptr if
    // not found.
    const wiki_page* find(
        const std::string& _title) const
    {
        for (const auto& page : pages)
        {
            if (page.title == _title)
            {
                return &page;
            }
        }

        return nullptr;
    };

    // pages_of_kind
    //   method: returns all pages of a given kind.
    std::vector<const wiki_page*>
    pages_of_kind(wiki_page_kind _kind) const
    {
        std::vector<const wiki_page*> result;

        for (const auto& page : pages)
        {
            if (page.kind == _kind)
            {
                result.push_back(&page);
            }
        }

        return result;
    };
};


///////////////////////////////////////////////////////////////////////////////
///             IV.   CONFIGURATION                                         ///
///////////////////////////////////////////////////////////////////////////////

// wiki_config
//   struct: controls what pages are generated and how they
// are named and categorized.
struct wiki_config
{
    // prefix prepended to all page titles (e.g., "API/"
    // produces "API/MyClass")
    std::string title_prefix;

    // default category applied to all pages; additional
    // per-kind categories are added automatically
    std::string default_category;

    // ---- generation toggles ----

    // when true, generate the module overview page
    bool generate_overview;

    // when true, generate individual class/struct pages
    bool generate_class_pages;

    // when true, generate namespace pages
    bool generate_namespace_pages;

    // when true, generate enum pages
    bool generate_enum_pages;

    // when true, generate the function index page
    bool generate_function_index;

    // ---- content toggles ----

    // when true, include access specifiers (public,
    // private, protected) in member listings
    bool show_access;

    // when true, include source file and line number
    // references in symbol documentation
    bool show_source_location;

    // when true, include inheritance hierarchy on class
    // pages
    bool show_inheritance;

    // when true, include the list of #include
    // dependencies on overview and class pages
    bool show_dependencies;

    wiki_config()
        : title_prefix()
        , default_category("API")
        , generate_overview(true)
        , generate_class_pages(true)
        , generate_namespace_pages(true)
        , generate_enum_pages(true)
        , generate_function_index(true)
        , show_access(true)
        , show_source_location(true)
        , show_inheritance(true)
        , show_dependencies(true)
    {
    };
};


///////////////////////////////////////////////////////////////////////////////
///             V.    MEDIAWIKI MARKUP HELPERS                              ///
///////////////////////////////////////////////////////////////////////////////

// wiki_markup
//   struct: static helper methods for generating MediaWiki
// markup fragments. Used internally by the default layouts
// and available for custom layouts.
struct wiki_markup
{
    // heading
    //   function: wraps text in a MediaWiki heading.
    // _level 1 = "= text =", 2 = "== text ==", etc.
    static std::string heading(
        const std::string& _text,
        int _level = 2)
    {
        std::string eq(_level, '=');

        return eq + " " + _text + " " + eq + "\n";
    };

    // bold
    //   function: wraps text in bold markup.
    static std::string bold(const std::string& _text)
    {
        return "'''" + _text + "'''";
    };

    // italic
    //   function: wraps text in italic markup.
    static std::string italic(
        const std::string& _text)
    {
        return "''" + _text + "''";
    };

    // code_inline
    //   function: wraps text in inline code markup.
    static std::string code_inline(
        const std::string& _text)
    {
        return "<code>" + _text + "</code>";
    };

    // code_block
    //   function: wraps text in a syntax-highlighted code
    // block.
    static std::string code_block(
        const std::string& _text,
        const std::string& _lang = "cpp")
    {
        return "<syntaxhighlight lang=\"" + _lang
            + "\">\n" + _text
            + "\n</syntaxhighlight>\n";
    };

    // link
    //   function: creates an internal wiki link.
    // _target is the page name, _display is optional
    // display text.
    static std::string link(
        const std::string& _target,
        const std::string& _display = "")
    {
        if (_display.empty())
        {
            return "[[" + _target + "]]";
        }

        return "[[" + _target + "|" + _display + "]]";
    };

    // category
    //   function: creates a category tag.
    static std::string category(
        const std::string& _cat)
    {
        return "[[Category:" + _cat + "]]\n";
    };

    // table_start
    //   function: opens a MediaWiki table.
    static std::string table_start(
        const std::string& _class = "wikitable")
    {
        return "{| class=\"" + _class + "\"\n";
    };

    // table_header
    //   function: creates a table header row.
    static std::string table_header(
        const std::vector<std::string>& _cols)
    {
        std::string row = "|-\n";

        for (std::size_t i = 0; i < _cols.size(); ++i)
        {
            row += "! " + _cols[i] + "\n";
        }

        return row;
    };

    // table_row
    //   function: creates a table data row.
    static std::string table_row(
        const std::vector<std::string>& _cells)
    {
        std::string row = "|-\n";

        for (std::size_t i = 0;
             i < _cells.size();
             ++i)
        {
            row += "| " + _cells[i] + "\n";
        }

        return row;
    };

    // table_end
    //   function: closes a MediaWiki table.
    static std::string table_end()
    {
        return "|}\n";
    };

    // horizontal_rule
    //   function: inserts a horizontal rule.
    static std::string horizontal_rule()
    {
        return "----\n";
    };

    // bullet
    //   function: creates a bullet list item.
    static std::string bullet(
        const std::string& _text,
        int _depth = 1)
    {
        return std::string(_depth, '*') + " "
            + _text + "\n";
    };

    // symbol_kind_string
    //   function: returns a human-readable string for a
    // code_symbol_kind value.
    static const char*
    symbol_kind_string(code_symbol_kind _kind)
    {
        switch (_kind)
        {
            case code_symbol_kind::namespace_decl:
                return "Namespace";
            case code_symbol_kind::class_decl:
                return "Class";
            case code_symbol_kind::struct_decl:
                return "Struct";
            case code_symbol_kind::union_decl:
                return "Union";
            case code_symbol_kind::enum_decl:
                return "Enum";
            case code_symbol_kind::enum_constant:
                return "Enumerator";
            case code_symbol_kind::function_decl:
                return "Function";
            case code_symbol_kind::method_decl:
                return "Method";
            case code_symbol_kind::constructor:
                return "Constructor";
            case code_symbol_kind::destructor:
                return "Destructor";
            case code_symbol_kind::operator_decl:
                return "Operator";
            case code_symbol_kind::variable_decl:
                return "Variable";
            case code_symbol_kind::field_decl:
                return "Field";
            case code_symbol_kind::typedef_decl:
                return "Typedef";
            case code_symbol_kind::type_alias:
                return "Type Alias";
            case code_symbol_kind::template_decl:
                return "Template";
            case code_symbol_kind::macro_definition:
                return "Macro";
            case code_symbol_kind::concept_decl:
                return "Concept";
            case code_symbol_kind::using_decl:
                return "Using";
            default:
                return "Symbol";
        }
    };

    // access_string
    //   function: returns a MediaWiki-formatted access
    // specifier badge.
    static std::string
    access_string(code_access _access)
    {
        switch (_access)
        {
            case code_access::public_access:
                return "{{access|public}}";
            case code_access::protected_access:
                return "{{access|protected}}";
            case code_access::private_access:
                return "{{access|private}}";
            default:
                return "";
        }
    };
};


